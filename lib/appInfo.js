const { version, name, bin} = require('../package')


//////////////////////////////////////////////////////////////////////////////
//
//////////////////////////////////////////////////////////////////////////////
const appInfo = function appInfo() {
    var self = this


    //////////////////////////////////////////////////////////////////////////////
    //
    //////////////////////////////////////////////////////////////////////////////
    self.getVersion = function getVersion() {
        return version
    }

    //////////////////////////////////////////////////////////////////////////////
    //
    //////////////////////////////////////////////////////////////////////////////
    self.getName = function getName() {
        return name
    }

    //////////////////////////////////////////////////////////////////////////////
    //
    //////////////////////////////////////////////////////////////////////////////
    self.getCLIName = function getCLIName() {
        return Object.keys(bin)[0]
    }
}
  
  
module.exports = appInfo